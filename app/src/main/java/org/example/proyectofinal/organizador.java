package org.example.proyectofinal;

public class organizador {
    private int ind_organizador;
    private String nombre_organizador;
    private String email_organizador;
    private String pass_organizador;

    public organizador(int ind_organizador, String nombre_organizador, String email_organizador, String pass_organizador) {
        this.ind_organizador = ind_organizador;
        this.nombre_organizador = nombre_organizador;
        this.email_organizador = email_organizador;
        this.pass_organizador = pass_organizador;
    }
}
